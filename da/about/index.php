<a name ="intro"></a>
<h2>Indtroduktion til projektet</h2>
<p>
SliTaz GNU/Linux er et frit og gratis operativsystem, som arbejder fuldstændigt i hukommelsen
startet fra et flytbart medie som en cdrom eller en USB-pind. Det er slankt, hurtigt og kan helt 
installeres på en harddisk. SliTaz distribueres i form af en LiveCD som du let kan brænde på en 
cdrom og starte fra. Når systemmet kører kan du udtage LiveCDen og benytte dit cddrev til andre 
opgaver. Live-systemmet giver en fuldt udrustet grafisk distribution og lader dig behold dine 
data og personlige indstillinger på et permanent medie. Systemmet kan udvides med Tazpkg 
pakkehåndteringen og sikkerhedsopdateringer tilbydes udviklings og stabile versioner.
</p>
<p>
SliTaz projektet tilbyder gratis teknisk support til brugere gennem 
<a href="../mailing-list.php">postlisten</a> (i18n listen)
og det <a href="http://forum.slitaz.org/">engelsksprogede forum</a>.
Du kan også kontakte os for mere information, foreslag eller kommentarer ved brug af
listen eller direkte med elektronisk post : &lt;devel snabel-a slitaz.org&gt;
</p>

<a name="overview"></a>
<h2>Overblik</h2>
<ul>
	<li>Rodfilsystem optagerer cirka 100 MB og ISO afbildning mindre end 30 MB.</li>
	<li>Web server klar til brug leveret af LightTPD med CGI og PHP understøttelse.</li>
	<li>Rejs ud på nettet med Midori webbrowser eller Retawq tekstbrowser.</li>
	<li>Lydunderstøttelse givet af Alsa mixer, lydafspiller og CD udtrækker/indkoder.</li>
	<li>Snakke, post og FTP klienter.</li>
	<li>SSH klient og server leveret med Dropbear.</li>
	<li>Databasemotor med SQLite.</li>
	<li>Værktøj til at gennerere en LiveUSB-pind.</li>
	<li>Værktøj til at skabe, redigere eller bænde CD og DVD skiver.</li>
	<li>Elegant skrivebord med Openbox kørende oven på Xorg/Xvesa (X server).</li>
	<li>Hjemmelevede grafiske kasser til kommandolinieværktøjer.</li>
	<li>2300 programpakker let installerbare fra filspejlet.</li>
	<li>Aktivt og venligt samfund.</li>
</ul>

<a name="website"></a>
<h2>International hjemmeside</h2>
<p>
SliTaz's hjemmeside er også tilgængelig på flere sprog og er del af
<a href="../../i18n.php">internationaliseringsprojektet</a>.
Hjemesiden tilbyder dig at <a href="../get/">hente</a> SliTaz
LiveCDen og hjælpe os med at udvikle projektet. Du kan undersøge 
<a href="../doc/">vejledningsområdet</a> for at lære at bruge SliTaz 
LiveCDen og indstille systemmet.
</p>

