<a name="art4taz"></a>
<h2>Grafik &amp; Design</h2>

<p>
Dette afsnit af hjemmesiden er didikeret til grafik for brug i SliTaz projektet.
Billeder er frie og du kan bruge dem til at lave dit eget skrivebordstapet eller 
vise et logo på din side for at promovere projektet.
SliTaz logoerne og ikoner kan hentes i SVG format. Du kan også finde nogle 
skærmbilleder <a href="screenshots.php">her</a> lavet med
mtPaint og Gimp. Grafikgruppen har sin egen afsnit på 
<a href="http://scn.slitaz.org/">SliTaz nætværket</a>.
</p>

<h3>Grafik for SliTaz</h3>
<p>
Som med mange andre ting i SliTaz er grafik, logoer, temaer og skrivebordstapeter
konstrukeret af samfundet og udvilkerne. Projektet ønsker altid at vedligeholde 
elegant og sobert kvalitetsdesign. 
At lave et logo, et tapet eller lignende er en god måde at deltage i projektet,
uden at få beskidte hænder af at kode eller læse korrektur på xHTML dokumenter. 
Hvis du vil lave grafik for SliTaz, eller vil oprette en konto på SliTaz netværket(SCN) for at 
lægge dit arbejde op, eller du vil benytte nogetgrafik online, er det højest anbefalessesværdigt,
at du først kontakter os via <a href="../mailing-list.php">postlisten</a>. 
Forsøg venligst ikke at sende filer der er alt for store.
</p>
<p>
For at overholde begrænsningerne på LiveCDen må billedet ikke være større
end 200 kilobyte og bør være på 1024x768 pixels. Hver ny frigivene version 
tilbyder et nyt tema/baggrund, som følger de samme retningslinier
og man er  ikke tvunget til at  bruge de værktøjer, som er distribueret med 
systemmet. På en LiveCD har du allerede mtPaint og på et installeret system 
kan du hente Gimp på filspejlet. Bemærk at JPG formatet fortrækkes.
</p>

