<h2 id="art4taz">アートワーク &amp; デザイン</h2>

<p>
	このセクションでは、SliTaz プロジェクトを対象としたアートやグラフィックスを扱っています。
    画像はフリーで、壁紙を作成したり、サイトのロゴを表示してプロジェクトの普及を助けたりすることができます。
	SliTaz のロゴとアイコンは、SVG 形式でダウンロードできます。
