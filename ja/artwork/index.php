</p>

<h3>SliTaz アートワーク</h3>

<p>
	SliTaz と同様に、グラフィックス、ロゴ、テーマ、壁紙は、コミュニティと開発者によって完全に構築されています。
このプロジェクトは、常にエレガントで心地よい質のデザインを維持することを好みます。
    ロゴや壁紙などを作ることは、手を汚くしたり、xHTMLを校正したりすることなく、プロジェクトに参加する良い方法です。
    SliTaz 用のアートワークを作成したい場合、または SCN プラットフォーム上で作業をアップロードするためのアカウントを作成する場合や、アートワークをオンラインで使用したい場合は、まず <a href="../mailing-list.php">メーリングリスト</a> でお問い合わせ下さい。
    大きすぎるファイルを送信しないようにして下さい。
</p>
<p>
	LiveCD の制約を維持するため、画像は 200k バイトを超えてはならず、1024x768 のサイズにする必要があります。
    各リリースの公開版は、同じガイドラインに従う新しいテーマ・背景を提供し、システムと共に配布されるツールを使用する必要があることは言うまでもありません。
    LiveCD を使用すると、mtPaint が既にインストールされていて、ミラー上には Gimp が見つかります。
    JPG形式が望ましいことに注意してください。
</p>

