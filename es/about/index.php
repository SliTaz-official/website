<a name ="intro"></a>
<h3>Introducción</h3>
<p>
SliTaz GNU/Linux es un sistema operativo gratuito que funciona 
directamente en la memoria del ordenador, a partir de dispositivos 
extraíbles como un cd-rom o una memoria USB. Es ligero, rápido y 
totalmente instalable en el disco duro. SliTaz se distribuye en formato 
imagen de LiveCD, que puedes grabar fácilmente en un cdrom desde el que 
arrancar el sistema luego. Con el sistema funcionando, puedes ya sacar 
el LiveCD y usar la unidad lectora para otras cosas. El LiveCD 
proporciona una distribución totalmente equipada, trabajando en un 
entorno gráfico y permitiéndote guardar sus datos en una memoria USB. 
El sistema puede ser extendido con el administrador de paquetez Tazpkg, 
e igualmente se proporcionan actualizaciones de seguridad para las 
versiones cooking y estable.
</p>
<p>
El proyecto SliTaz proporciona además soporte técnico gratuito para los 
usuarios mediante la <a href="../mailing-list.php">Lista de correo</a> 
(lista i18n) y el <a href="http://forum.slitaz.org/">Foro en Inglés</a>. 
Puedes también contactar con nosotros para más información, sugerencias 
o comentarios, mediante la lista de correo o directamente por correo a
&lt;devel at slitaz.org&gt;.
</p>

<a name="overview"></a>
<h3>Principales características</h3>
<ul>
    <li>El sistema de ficheros raíz ocupa sobre los 100 MB y la imagen ISO menos de 30 MB.</li>
    <li>Servidor Web de Lighttpd, con soporte para CGI y PHP listo para usar.</li>
    <li>Navega por internet con Midori o Retawq, en modo texto.</li>
    <li>Soporte para sonido mediante el reproductor de audio y el mezclador de Alsa, así como ripper y encoder de CDs.</li>
    <li>Correo, chat y clientes FTP.</li>
    <li>Cliente y servidor SSH de Dropbear.</li>
    <li>Motor de base de datos de SQLite.</li>
    <li>Generación de dispositivo LiveUSB.</li>
    <li>Herramientas para crear, editar o quemar imágenes de CDs o DVDs.</li>
    <li>Escritorio elegante con Openbox ejecutándose sobre Xorg/Xvesa (servidor X).</li>
    <li>Cuadros de diálogo caseros para utilidades de línea de comandos.</li>
    <li>2300 paquetes fácilmente instalables desde cualquier mirror.</li>
    <li>Comunidad activa y acogedora.</li>
</ul>

