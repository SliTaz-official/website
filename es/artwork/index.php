<h2 id="art4taz">Arte y Diseño</h2>

<p>
Esta sección de la web está dedicada al arte y los gráficos corporativos
del proyecto SliTaz. Las imágenes son gratuitas, podiendo usarlas para
hacer tu propio fondo de pantalla o mostrar un logo en tu página web
para publicitar el proyecto. Los logos e iconos de SliTaz están
disponibles en muchos tamaños y se pueden descargar directamente a
través del directorio <a href="/images/artwork/">artwork</a>. 
También encontrarás aquí algunas <a href="/en/artwork/screenshots.php">capturas</a> 
de pantalla hechas con mtPaint y The Gimp.
</p>

<h3>Artwork para SliTaz</h3>

<p>
Al igual que muchas cosas en SliTaz, los gráficos, logos, temas y fondos
de pantalla están hechos por la comunidad y los desarrolladores. Al
proyecto le gusta mantener un diseño de calidad, sobrio y elegante.
Hacer un logo, un fondo de escritorio o lo que sea es una buena manera
de participar en el proyecto sin tener que ensuciarte las manos
programando o documentando en xHTML. Si quieres contribuir con artwork,
o usar el que está disponible, recomendamos encarecidamente contactarnos
primero en la <a href="../mailing-list.php">Lista de Correo</a>. 
Trata de no enviar ficheros demasiado grandes.
</p>
<p>
Cada nueva versión publicada trae un nuevo tema y fondo de escritorio.
Estos temas deben poder ser creados usando las herramientas distribuidas
con el sistema. Para mantener el formato del LiveCD, la imagen no debe
exceder de 200 kilobytes y debe ser de 1024x768. El formato libre PNG es
el preferido. El LiveCD incluye la aplicación mtPaint. En un sistema
instalado, Gimp puede ser instalado desde cualquier mirror.
</p>

