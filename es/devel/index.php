<a name="devel"></a>
<h2>Involúcrate</h2>
<p>
	SliTaz es una distribución comunitaria y de código abierto. Todo el
	mundo es bienvenido a unirse y contribuir. Para usuarios, manitas y
	desarrolladores, siempre hay algo que hacer, como verificar
	o escribir documentación, enviar errores o parches a la lista de correo,
	conseguir acceso al wok y añadir algunos paquetes, o simplemente ayudar
	a otros en la lista de correo o en el <a href="http://forum.slitaz.org/">foro</a>. 
	SliTaz tiene repositorios de Mercurial sobre un sistema también SliTaz. 
	Los desarrolladores pueden solicitar un nuevo repositorio si fuera necesario 
 	y cualquiera puede contribuir con acceso de escritura para corregir errores 
	tipográficos, añadir scripts, etc.
</p>
<p>
	SliTaz es una comunidad pequeña que escucha a sus usuarios. Hay varios
	desarrolladores activos en el <a href="http://forum.slitaz.org/">foro</a>
 	y en la <a href="../mailing-list.php">lista de correo</a>.
</p>
<p>
	Incluso para artistas hay una web dedicada. Simplemente crea una cuenta
	y envía tus gráficos o imágenes. La web está gestionada por la
	comunidad. Si quieres echar una mano administrando la web, por favor
	contacta con un colaborador o escribe a la lista de correo. Únete al
	<a href="http://scn.slitaz.org/groups/artwork/">Artwork group</a> de la 
	Comunidad SliTaz.
</p>

