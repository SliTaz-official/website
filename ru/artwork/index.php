<h2 id="art4taz">Графика и дизайн</h2>

<p>Этот раздел сайта посвящен графике проекта SliTaz. Все изображения бесплатны, на их основе вы можете сделать свои собственные обои или разместить логотип на вашем сайте, чтобы помочь популяризации проекта. Логотипы и значки SliTaz доступны для скачивания в формате SVG. Здесь вы также найдете некоторые <a href="screenshots.php">скриншоты</a>, сделанные с помощью mtPaint и Gimp. Команда Artwork имеет свою платформу в <a href="http://scn.slitaz.org/">сети сообщества SliTaz</a>.</p>


<h3>Графика для SliTaz</h3>

<p>Как и многое другое в SliTaz, графика, логотипы, темы и обои полностью созданы сообществом и разработчиками. Проекту нравится поддерживать всегда элегантный, здравый, качественный дизайн. Создание логотипа, фонового рисунка и т.п. — это хороший способ участия в проекте, не вникая в кодирование или проверку документов xHTML. Если вы хотите нарисовать картинку для SliTaz, или, если хотите создать учетную запись на платформе SCN для того, чтобы загрузить свои работы, или хотите использовать некоторые изображения в интернете, мы настоятельно рекомендуем вам обратиться сначала в <a href="../mailing-list.php">рассылку</a>. Пожалуйста, постарайтесь не отправлять слишком большие файлы.</p>

<p>В силу ограничений LiveCD, изображение не должно превышать 200 килобайт и должно иметь размер 1024×768. Конечно же, каждая версия предлагает новую тему и фон, которые следуют тем же правилам и, само собой разумеется, что при этом должны использоваться программы, поставляемые с системой. На LiveCD у вас уже есть mtPaint, также вы можете найти Gimp на зеркале. Обратите внимание, что формат JPG является предпочтительным.</p>


