<a href="http://doc.slitaz.org/de:handbook:start">deutsche Dokumentation</a>
vollständig in das Wiki verlegt. Weitere Hilfe auf deutsch erhalten 
Sie sonst im <a href="http://forum.slitaz.org/">Forum</a>.
</p>

