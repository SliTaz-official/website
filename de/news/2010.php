<h2>2010</h2>

<div class="news">

<ul>
	<li id="d20101104">
		<strong>4. November 2010 - Neue Entwicklungs-Version (20101104)</strong>
	<p>	Das SliTaz-Team ist erfreut, die Freigabe einer neuen Entwicklungsversion mit mehr
	als 2600 Paketen ankündigen zu können.
	Sie enthält den Linux-Kern 2.6.34 und wurde mit dem neuen Werkzeugkasten unter
	Verwendung von glibc 2.11.2 und gcc 4.5.1 erstellt. Xorg wurde in vollem Umfang
	auf die Version 1.9.2 aktualisiert. Das Direktstart-ISO-Abbild enthält Midori, das
	Audio-Abspielprogramm Deadbeef und viele andere Anwendungen. Tazpkg und Tazctrlbox können
	jetzt mit gettext übersetzte Meldungen ausgeben. Auch wurden weitere Lokalisierungen
	hinzugefügt. Die Version enthält auch viele Fehlerkorrekturen und Verbesserungen,
	dank harter Arbeit in den letzten sechs Monaten.
	Die Entwicklungs-Version steht in den offiziellen <a href="/de/get/">SliTaz-Depots</a>
	zum Transfer bereit.</p>
	<p>Rückmeldungen über das Diskussionsforum oder das Forum sind willkommen.</p>
	</li>

	<li>
		<strong>8. Mai 2010 - Sommer der Dokumentation</strong>
	<p>Das SliTaz-Team bereitet einen „Summer of Documentation“ vom 10.&nbsp;Mai bis zum 10.&nbsp;Juni vor.
	Hauptziel ist die Zentralisierung, Überprüfung und Aktualisierung aller über das Internet zugänglichen
	Dokumentation.
	Einzelheiten darüber finden Sie bei <a href="http://doc.slitaz.org/">doc.slitaz.org</a>.
	Wir wünschen uns, dass die SliTaz-Nutzer uns durch Schreiben neuer oder Aktualisierung bestehender
	Dokumentation unterstützen.
	</p>
	</li>

	<li>
		<strong>28. März 2010 - Freigabe von SliTaz GNU/Linux 3.0</strong>
	<p>	Das SliTaz-Team ist stolz, nach einjähriger Entwicklung die Freigabe der neuen stabilen
	Version SliTaz GNU/Linux 3.0 ankündigen zu können. Diese Version ist einfacher,
	schneller, anpassbar, mächtiger und dennoch unglaublich klein.</p>
	<p>Die „core“-Variante enthält eine vollständige grafische Oberfläche auf der Basis von Xorg 7.4,
	Openbox, LXDE-Komponenten und selbsterstellten Programmen. Damit können Sie einfach Verbindungen
	über das Internet aufbauen, mit dem Webbrowser Midori durch das Internet stöbern, Musik hören
	oder Fotos verwalten.</p>
	<p>Die Standard-„core“-Variante ist in einem ISO-Abbild von 30&nbsp;MB Größe enthalten.
	Varianten von Direkstart-ISO-Abbildern gibt es schon ab 8&nbsp;MB.
	Diese stabile Version wurde mit dem neuen Werkzeugkasten, der GCC 4.4.1 enthält, und dem
	Linux-Kern 2.6.30.6 erstellt.
	Genauere Informationen über die stabile Version	finden Sie in der
	<a href="/de/doc/releases/3.0/relnotes.de.html">Freigabemitteilung</a>.
	SliTaz GNU/Linux 3.0 steht in den offiziellen <a href="/de/get/#stable">Depots</a> bereit.</p>
	</li>

	<li>
		<strong>14. März 2010 - Serie von Freigabekandidaten und Solutionslinux 2010 (Paris)</strong>
	<p>Da wir uns der Freigabe der nächsten stabilen Version nähern, starten wir eine neue
	Serie von Freigabekandidaten für die „core“-Variante der ISO-Abbilder.
	Die wichtigste Änderung besteht darin, dass Midori jetzt der Standard-Webbrowser ist. Damit
	wird das Nutzungserlebnis verbessert und die Gesamtgröße von SliTaz kann bei 30&nbsp;MB bleiben.
	Midori ist ein moderner Browser auf Basis der „webkit engine“ -
	er ist schnell, einfach in der Handhabung und gut in die grafische Oberfläche von SliTaz integriert.</p>
	<p>Dieses aktualisierte ISO-Abbild enthält eine Menge Fehlerkorrekturen und korrigierter Abhängigkeiten.
	Auch die selbstgemachten Programme und Kommandoprozeduren wurden verbessert.
	Das neue ISO-Abbild kann (in einer Größe von 30&nbsp;MB) als
	<a href="http://mirror.slitaz.org/iso/cooking/slitaz-cooking.iso">slitaz-cooking.iso</a>
	aus den SliTaz-Depots transferiert werden.</p>
	<p>Nebenbei bemerkt: Das SliTaz-Projekt wird auf der
	<a href="http://www.solutionslinux.fr/">Solutionslinux</a> 2010 nächste
	Woche in Paris vertreten sein. Dort können Sie mehr über das Projekt erfahren und die
	Mitglieder des Vereins und das Entwicklungsteam kennen lernen.</p>
	</li>

	<li>
		<strong>21. Februar 2010 - Neue Entwicklungs-Version (20100221)</strong>
	<p>	Das SliTaz-Team ist stolz, die Freigabe einer neuen Entwicklungsversion 
	ankündigen zu können.
	Dies ist der letzte Entwicklungs-Freigabekandidat vor der Freigabe der Version 3.0.
	Die „core“-Variante des ISO-Abbilds hat eine Größe von 27&nbsp;MB und enthält
	viele Änderungen und Verbesserungen. Diese Entwicklungs-Version verwendet
	Xorg als X-Server statt Xvesa und unterstützt UTF-8 in vollem Umfang. Auf
	einer aufgeräumten grafischen Oberfläche werden viele Anwendungen für alltägliche
	Aufgaben und den täglichen Gebrauch bereitgestellt.
	Netsurf ersetzt firefox als Standard-Webbrowser.
	Nichtsdestotrotz gibt es auch eine 29&nbsp;MB große Variante mit Firefox, Audio- und
	Drahtlosnetzwerk-Unterstützung in den offiziellen Depots.</p>
	<p>Zu den Aktualisierungen und Verbesserungen: tazpkg ist schneller, Kommandoprozeduren
	für die Systemeinleitung wurden aktualisiert und selbstgemachte Grafikfenster wurden
	verbessert. Wifibox verwendet  <code>awk</code> zum besseren Absuchen des Netzwerks.
	In den Kommandoprozeduren für die Systemeinleitung wurde die WPA-Unterstützung verbessert. Die
	Zeit für das Systemladen wird für Leistungsmessungen protokolliert.
	Die SliTaz-Installationsprozedur wird jetzt versuchen, Dateien von einem
	USB-Datenträger zu verwenden, wenn kein optisches Laufwerk verfügbar ist, das Einrichten
	einer Standard-Benutzerkennung ermöglichen und es ist möglich, ein Kennwort für die Benutzerkennung
	root zu	vergeben und eine eigene Partition für das Verzeichnis <code>/home</code> zu verwenden.
	Schließlich wird auch das Anpassen eines Direkstart-ISO-Abbildes viel einfacher: ein modifiziertes
	Direkstart-ISO-Abbild kann erzeugt werden durch Laden, Modifizieren und mit <code>writefs</code>
	(über die grafische Oberfläche von Tazlito oder per Kommandoeingabe) ein neues Direkstart-ISO-Abbild
	schreiben.</p>
	<p>Wie üblich sind die Varianten in den offiziellen <a href="/de/get/#cooking">SliTaz-Depots</a>
	erhältlich. Rückmeldungen können über das SliTaz-Forum abgegeben werden.</p>
	</li>
</ul>

