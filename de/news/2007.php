<ul>

	<li>
		<strong>16. Dezember 2007 - Neues Forum</strong>
	<p>
	Es gibt jetzt für SliTaz ein <a href="http://forum.slitaz.org/forum">englisches Forum</a>,
	wo englischsprachige Nutzer Unterstützung erhalten können.
	Auch wenn das Projekt aus Frankreich stammt, haben uns viele Rückmeldungen
	und Anfragen aus der englisch-sprechenden Nutzergemeinschaft erreicht, denen
	wir gerecht werden wollen. Das Forum hat eine eigene Adresse:
	<a href="http://forum.slitaz.org/">forum.slitaz.org</a>. Die standardmäßige
	Spracheinstellung ist französisch, kann aber nach der Anmeldung geändert werden.
	Wir arbeiten momentan auch an den Mängeln der englischen Dokumentation, wofür jede Hilfe
	natürlich herzlich willkommen ist.
	</p></li>

	<li>
		<strong>06. Dezember 2007 - In zwei Tagen ein neues ISO-Abbild, das doppelt so schnell geladen wird!</strong>
	<p>
	Mit der neuen lzma-Kompression verringert sich die Zeit für den Ladevorgang drastisch. Nach zwei Tagen
	harter Arbeit steht eine neue Version zur Verfügung, die nur die halbe Zeit für den
	Ladevorgang benötigt. Es steht jetzt ein neues ISO-Abbild mit einer lzma-Korrektur für den Linux-Kern
	und einigen Fehlerkorrekturen für die Ausgabe der Meldungen beim Systemladen zur Verfügung.
	Tests ergaben, dass das Direktstart-ISO-Abbild jetzt auf neueren Rechnern zwischen 20 und 30 Sekunden,
	auf älteren 60 bis 80 Sekunden zum Laden benötigt.
	<a href="/de/get/#cooking">Transferieren der aktuellen Entwicklungsversion</a>.
	</p>
	</li>

	<li>
		<strong>04. Dezember 2007 - Neue Entwicklungs-Version mit Audio-Unterstützung</strong>
	<p>
	Das SliTaz-Team ist stolz, die Freigabe einer neuen Entwicklungsversion ankündigen zu können.
	Dies ist noch nicht die erwartete stabile Version, da wir zunächst eine bessere Hardware-Unterstützung,
	wie Audio- und PCMCIA-Unterstützung in das ISO-Abbild integrieren wollten.
	Dadurch ist die Planung geändert worden. Die Freigabe der stabilen Version ist jetzt im März 2008
	zu erwarten, ein Jahr nach der ersten Veröffentlichung von SliTaz. In dieser Zeit werden wir Zeit
	haben, eine grundsolide, stabile Version mit all den neuen Funktionen und mehr als 240 Paketen
	zu erstellen.<br />
	Die vorliegende neue Entwicklungs-Version enthält Alsa und den zugehörigen Mixer, Alsaplayer, mpg123, Gcolor2,
	pcmciautils und einen neuen Linux-Kern. Dazu gibt es noch neue Optionen, die beim Laden von SliTaz
	angegeben werden können und isapnptools	für sehr alte Hardware wie IBM GL300 6272/6282 oder P133/32M.
	All das passt in das ISO-Abbild mit einer Größe von 22,4 MB.
	Die „Simple Light Incredible Temporary Autonomous Zone“ eben! Dieses ISO-Abbild ist aus dem
	<a href="/de/get/#cooking">Depot</a> zu beziehen. Viel Vergnügen damit!
	</p>
	</li>

	<li>
		<strong>06. November 2007 - Letzte Entwicklungs-Version vor der Freigabe von 1.0</strong>
	<p>
	Das SliTaz-Team ist stolz, die Freigabe der letzten Entwicklungsversion vor der stabilen Version 1.0
	ankündigen zu können.
	Neu sind in dem 24,7 MB großen ISO-Abbild PHP 5.2.5, Grsync, ISO Master 1.2,
	Busybox 1.7.2 und vieles mehr. Auch gibt es ein neues Design und Hintergrundbild für JWM, sowie
	ein Tango-Symbol.
	Dieses ISO-Abbild ist kostenfrei aus dem <a href="/de/get/#cooking">TuxFamily-Depot</a> zu beziehen.
	Rückmeldungen jeder Art sind willkommen; Sie können die „Mailing“-Liste nutzen oder eine normale
	E-Mail an &lt;devel at SliTaz.org&gt; senden.
	</p>
	</li>

	<li>
		<strong>12. Oktober 2007 - Neue Entwicklungs-Version und Erhöhung der Stabilität</strong>
	<p>
	Das 24 MB großen Direktstart-ISO-Abbild enthält Firefox 2.0.0.7,
	den Linux-Kern 2.6.22.9, Rsync, ISO Master 1.1, LightTPD 1.4.18, SQLite 3.5.1,
	ein neues Design und Hintergrundbild. Ebenso wurde die Paketverwaltung mit
	Tazwok und den <code>slitaz-tools</code> ausgebaut. Mit Transmission 0.82 ist es nun möglich
	„Torrent Filesharing“ zu nutzen.
	Dieses ISO-Abbild ist kostenfrei aus dem <a href="/de/get/#cooking">TuxFamily-Depot</a> zu beziehen.
	</p>
	</li>
</ul>

