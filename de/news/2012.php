<h2>2012</h2>

<div class="news">

<ul>
	<li id="d20120410">
		<strong>10. April 2012 - Freigabe von SliTaz GNU/Linux 4.0</strong>
	<p>
		Das SliTaz-Team ist stolz, die Freigabe der neuen stabilen
		Version SliTaz GNU/Linux 4.0 ankündigen zu können. In zwei Jahren
		gemeinschaftlicher Entwicklung ist ein zuverlässiges System mit mehr als
		1000 Paketen entstanden, das noch schneller als bisher geladen werden kann.
	</p>
	<p>
		SliTaz enthält in einem ISO-Abbild von 35&nbsp;MB eine vollständige grafische Oberfläche,
		die auf LXDE und Openbox aufbaut und ganz in 192&nbsp;MB Arbeitsspeicher Platz findet. Das 
		neue 4in1-ISO-Abbild kann auf einer Platte mit nur 48&nbsp;MB installiert werden. Beim
		Urladen wird automatisch die enthaltene Variante ausgewählt, die zu der vorhandenen
		Hardware passt.
	</p>
	<p>
		Die selbstgemachten Programme wurden erweitert. Tazpkg enthält ein neues
		Benachrichtigungssystem und ist trotz der vergrößerten Anzahl von Paketen schneller
		geworden. Tazpanel ermöglicht eine neue, zentralisierte Systemverwaltung. 
		Genauere Informationen finden Sie in der
		<a href="/de/doc/releases/4.0/relnotes.de.html">Freigabemitteilung</a>.
		SliTaz GNU/Linux 4.0 steht im offiziellen
		<a href="http://mirror.slitaz.org/iso/4.0/slitaz-4.0.iso">Depot</a> bereit.
	</p>
	</li>

	<li id="d20120303">
		<strong>3. März 2012 - SliTaz 4.0-RC2 ist freigegeben</strong>
	<p>
		Das SliTaz-Team ist erfreut, einen neuen Freigabekandidaten 4.0-RC2
		ankündigen zu können.
		Viel Arbeit wurde investiert, Fehler aus dem Freigabekandidaten RC1
		und in den selbstgemachten Programmen wie TazPKG, TazPanel und TazUSB
		zu beheben. Die Verwaltung von Drahtlosnetzwerken wurde mit 
 		TazPanel oder dem neuen wifibox erleichtert. TazPKG wurde besser
		in die grafische Oberfläche integriert und hat ein neues Benachrichtigungssystem.
	</p>
	<p>
		Diese neue Version enthält ein vollständiges Xorg-Paket und DRI-Unterstützung ebenso
		wie die nv-, intel-, geode- und vesa-Treiber, die ohne Konfigurierung funktionieren.
		Alle Pakete wurden neu erzeugt und wiederum 2&nbsp;MB in der „core“-Variante
		des Direkstart-ISO-Abbildes eingespart. Jetzt sind daher auch Lxrandr, Parcellite und
		BiTorrent darin enthalten.
	</p>
	<p>
		Wir bedanken uns bei allen, die den ersten Freigabekandidaten getestet haben; mit diesem
		Freigabekandidaten RC2 sind wir jetzt nahe an der künftigen Version 4.0 von SliTaz,
		deren Freigabe in zwei Wochen geplant ist.
		Das ISO-Abbild des Freigabekandidaten RC2 (35,1&nbsp;MB) steht im Depot bereit:
		<a href="http://mirror.slitaz.org/iso/cooking/slitaz-4.0-RC2.iso">SliTaz 4.0-RC2</a>
	</p>
	</li>

	<li id="d20120223">
		<strong>23. Februar 2012 - SliTaz 4.0-RC1 ist freigegeben</strong>
	<p>
		Das SliTaz-Team ist erfreut, die Verfügbarkeit des ersten
		Freigabekandidaten für SliTaz 4.0 ankündigen zu können.
		SliTaz kann in 10 bis 12 Sekunden auf ziemlich neuer Hardware geladen werden.
		Mit dem neuen, 4in1-Direkstart-ISO-Abbild kann SliTaz mit einer vollständigen
		grafischen Oberfläche auf einem Rechner mit nur 48&nbsp;MB Arbeitsspeicher
		installiert werden. Beim Laden von dem 4in1-Direkstart-ISO-Abbild wird
		automatisch die Größe des Arbeitsspeichers erkannt und, je nach Größe, SliTaz
		im Textmodus, mit einer minimalen X-Umgebung oder mit vollständiger
		grafischer Oberfläche geladen.
	</p>
	<p>
		SliTaz 4.0 wird mehr als 3000 gut getestete Paket im Depot haben. Mit der
		Reihe seiner Freigabekandidaten können wir die neuesten Fehler verfolgen.
		Alle Pakete der letzten Entwicklungsversion wurden zweimal neu erzeugt,
		um die Qualität sicherzustellen.
	</p>
	<p>
		Beim Laden des Direkstart-ISO-Abbildes dieses Freigabekandidaten RC2 wird
		mit einem neuen grafischen Menü nach der gewünschten Sprachumgebung und
		der Tastaturbelegung gefragt. Das Design der grafischen Oberfläche ist neu.
		Die neue Kommandoprozedur für die Installion kann über TazPanel verwendet
		werden. Das neue Direkstart-ISO-Abbild steht im Depot bereit:
		<a href="http://mirror.slitaz.org/iso/cooking/slitaz-4.0-RC1.iso">SliTaz 4.0-RC1</a>
	</p>
	</li>
</ul>

