<h2>Estatuto</h2>


<h3>Geral</h3>

<h4>Artigo 1: Nome, localização e duração</h4>

<p>
A "Associação SliTaz GNU/Linux" é uma organização sem fins lucrativos, 
política e religiosamente neutra, corporativamente organizada de acordo
com o artigo 60 do código civil Suiço. Sua matriz baseia-se na cidade de
Montpreveyres, Suiça. Sua duração é ilimitada.
</p>

<h4>Artigo 2: Propósito</h4>

<p>
O objetivo da associação é manter, prover suporte e promover o SliTaz
GNU/Linux, uma distribuição focada em fornecer um sistema operacional
livre, estável, leve e portável.
</p>

<h3>Membros</h3>

<h4>Artigo 3: Membros</h4>

<p>
Indivíduos ou grupos interessados nos objetivos da associação podem se 
tornar membros. Os pedidos de associação são enviados ao comitê, que 
admite novos membros e informa a Assembléia Geral.
</p>

<h4>Artigo 4: Admissão, desistência e exclusão</h4>

<p>
Cada membro deve reconhecer o Estatuto da Associação e as decisões dos
organizadores. Alguém torna-se membro ao pagar a taxa de associação fixada.
</p>
<p>
Qualquer desistência deve ser comunicada por escrito ao Comitê. O 
membro desistente deve pagar sua taxa de associação referente ao ano 
corrente.
</p>
<p>
O Comitê pode se decidir pela exclusão de membros por qualquer razão 
válida, informando o fato à Assembléia Geral. Qualquer membro pode 
apresentar uma apelação à Assembléia, menos aqueles que tenham
desistido de sua associação.
</p>

<h4>Artigo 5: Responsabilidade</h4>

<p>
Membros associados não podem ser responsabilizados por quaisquer
comprometimentos feitos pela Associação, a não ser àqueles relacionados
exclusivamente a ela.
</p>

<h3>Órgãos</h3>

<h4>Artigo 6: Órgãos</h4>

<p>
Os órgãos da Associação são:
</p>
<ul>
	<li>A Assembléia Geral</li>
	<li>O Comitê</li>
	<li>O Conselho de Auditoria</li>
</ul>

<h4>Artigo 7: A Assembléia Geral</h4>

<p>
O órgão supremo da Associação é a Assembléia Geral. Ocorre a cada ano e é
formada por todos os membros da Associação. A data e a pauta devem ser
informadas por escrito a todos os membros com pelo menos 8 semanas de
antecipação. Ela pode ocorrer ao mesmo tempo do "SliTazDays" ou por
vídeo conferência.
</p>
<p>
A Assembléia Geral constitui-se assim que 6 membros com direito a voto 
estejam presentes, no local do encontro ou por vídeo conferência. Ela
objetiva:
</p>
<ul>
	<li>Eleger o Comitê e Conselho de Auditoria</li>
	<li>Aprovar o relatório apresentado pelo Comitê</li>
	<li>Discutir a política geral da Associação</li>
	<li>Deliberar sobre as propostas feitas a ela</li>
	<li>Aprovar as contas e colocar o orçamento em votação</li>
	<li>Dispensar o Comitê e o Conselho de Auditoria</li>
	<li>Definir o valor da contribuição anual</li>
	<li>Aprovar e fazer emendas ao estatuto</li>
	<li>Dissolver a Associação</li>
</ul>
<p>
À cada membro, individual ou coletivamente, é garantido um voto. A 
acumulação de votos é proibida. Em caso de empates, o voto de quem
preside a reunião é decisivo. O voto é dado, no momento da eleição,
pelo ato de levantar-se as mãos ou via email, 10 dias antes do encontro.
A votação pode ser secreta se um quinto dos membros assim requerirem.
</p>
<p>
A Assembléia Geral é presidida por um membro do Comitê. Propostas 
individuais devem ser recebidas pelo Comitê até 5 dias antes da data da 
Assembléia.
</p>

<h4>Artigo 8: Assembléia Extraordinária</h4>

<p>
Assembléias Extraordinárias podem ser marcadas pelo Comitê ou a pedido
de um quinto dos membros com direito à voto.
</p>

<h4>Artigo 9: O Comitê</h4>

<p>
O Comitê é autônomo e é formado por 3 a 5 membros. É eleito pela 
Assembléia Geral, porém o que determina sua atuação são suas próprias 
decisões. Para tarefas especiais, o Comitê deve designar um membro para
dirigir os grupos de trabalho. A duração das funções de todos os membros
do Comitê é indeterminada, porém pode ser revogada na Reunião Geral, por
justa causa e após a análise de relatório apresentado por uma comissão.
</p>
<p>
O Comitê dirige as atividades da Associação. Reuniões são feitas de 
tempos em tempos, quando necessárias e por requisição de um dos membros.
Também representa a Associação em relações com terceiros. A assinatura
de um membro do Comitê compromissa a própria Associação.
</p>
<p>
O Comitê deve:
</p>
<ul>
	<li>Tomar todas as providências para garantir os objetivos da associação</li>
	<li>Convocar os Encontros Gerais Ordinários e Extraordinários</li>
	<li>Tomar decisões relativas à admissão e exclusão de membros</li>
	<li>Garantir a aplicação do estatuto</li>
	<li>Administrar as propriedades da Associação</li>
	<li>Empregar uma equipe e voluntários</li>
</ul>

<h4>Artigo 10: Conselho de Auditoria</h4>

<p>
A Assembléia Geral deve eleger dois auditores pelo período de dois anos, 
que podem ser reeleitos. São responsáveis pela auditoria da Associação e
por apresentar os resultados de sua análise em um relatório se assim
forem solicitados pela Assembléia Geral.
</p>

<h3>Finanças</h3>

<h4>Artigo 11: Recursos</h4>

<p>
Os Recursos da associação provêm de:
</p>
<ul>
	<li>Taxas de Associação</li>
	<li>Produtos específicos</li>
	<li>Doações</li>
</ul>

<h4>Artigo 12: Contribuições</h4>

<p>
As contribuições de membros, individualmente ou em grupos, são definidas
pela Reunião Geral. Membros do Comitê e voluntários não pagam taxas ou
contribuições.
</p>

<h3>Disposições Finais</h3>

<h4>Artigo 13: Emendas ao Estatuto</h4>

<p>
Modificações no Estatuto somente serão aceitas após decisão do Comitê ou
da Assembléia Geral, pela maioria de 2/3 dos votos de membros votantes,
desde que a emenda esteja relacionada na pauta da Assembléia.
</p>

<h4>Artigo 14: Dissolução</h4>

<p>
A dissolução da Associação pode ser decidida somente mediante o pedido
do Comitê ou pela metade dos membros com direito a voto, em uma Assembléia
Extraordinária convocada especialmente para este fim. A decisão pela 
dissolução requer 2/3 dos votos de membros votantes. Caso a dissolução
se confirme, uma Reunião Geral Extraordinária decidirá sobre o destino
dos fundos levantados pela associação, assegurando para que cumpram seus
propósitos.
</p>

<h4>Artigo 15: Homologação</h4>

<p>
O Estatuto foi aprovado pela Assembléia Geral constituida em 31 de março
de 2009, passando a ter efeito a partir desta data. Para qualquer outra
questão que não estiver incluida neste Estatuto, por favor consultar
o Artigo 60 do Código Civil Suiço.
</p>

