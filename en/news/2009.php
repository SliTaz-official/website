	any questions. So feel free to visit us! Information can be found on
	<a href="http://labs.slitaz.org/wiki/events/2009-linuxdays">SliTaz Labs</a>
	and the <a href="http://2009.linuxdays.ch/">2009.linuxdays.ch</a> website.
	</p></li>

	<li>
		<strong>16 April 2009 - SliTaz GNU/Linux 2.0 release</strong>
	<p>
	The SliTaz project are proud to announce the release of the new stable
	version of the distribution - SliTaz GNU/Linux 2.0. This new version is the
	result of one year of community work and provides an ultra lightweight, fast,
	scalable and stable operating system. SliTaz provides a complete graphical
	desktop in less than 30MB, applications required for a reliable and robust
	server environment and 1400 easily installable software packages in a few
	clicks of the mouse. This new version offers the automounting of partitions
	(NTFS compliant), wifi support, an office respecting the Freedesktop.org
	standards (drag and drop), network booting with gPXE and
	<a href="http://boot.slitaz.org/">boot.slitaz.org</a>, automatic detection
	of USB and PCI devices, a GUI package manager and control center, and various
	graphical tools to manage the distribution. The creation of USB media and
	bootable LiveCD flavors is also simplified and greatly improved. The core
	LiveCD includes Firefox 3.0.8, gFTP, LostIRC, ePDFView, mtPaint, Transmission,
	Osmo and Alsaplayer. The website documentation has been updated and you can read the
	<a href="../doc/releases/2.0/relnotes.en.html">release notes</a> included in
	the distribution for more details on this stable version. SliTaz GNU/Linux
	2.0 is now <a href="../get/">downloadable</a> from the official mirrors of
	the project.
	</p></li>

	<li>
