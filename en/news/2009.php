	<li>
		<strong>28 Feb 2009 - New Cooking 20090228</strong>
	<p>
	Getting ready for the next stable release! - The SliTaz project is proud
	to publish a new Cooking version with a lot of bug fixes, updates, improvements
	and new features. PCI/USB device auto-detection is now handled by Tazhw and
	firmware can be installed in one click through a graphical interface.
	Tazndis also has a simple GUI which lets you manage Windows drivers in a
	user friendly interface. The graphical packages manager is also much simpler
	to use and LiveUSB media such as a USB key can easily be created through a box.
	Netbox displays all interfaces and the new Wifibox can connect to
	wireless essids, manage favorite networks and configure card drivers
	manually or through Tazhw. The desktop has a GUI to enable or disable
	applications started with an Openbox session and Ivman with desktopkbox
	provides a lightweight notification system. You can
	<a href="../get/#cooking">download</a> this new ISO image from one of the
	official mirrors of the website.
	</p></li>
