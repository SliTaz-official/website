	track the release of regular updates. Register to participate and
	leave a comment. The project is also proud to announce 2 new mirrors:
	In Switzerland, hosted by <a href="http://mirror.switch.ch/">SWITCHmirror</a>
	(10 Gbits/s) and hosted in China by <a href="http://www.lupaworld.com">LupaWorld</a>,
	thanks for supporting us.
	</p></li>

