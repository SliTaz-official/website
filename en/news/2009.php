<h2>2009</h2>

<ul>
	<li>
		<strong>4 November 2009 - New Cooking 20091104</strong>
	<p>SliTaz contributors are proud to announce the availability of a new Cooking
	ISO image based on over 2100 packages available in the SliTaz repository.
	The full system has been rebuilt with a new toolchain using Glibc 2.10.1
	and GCC 4.4.1. This new Cooking uses the Linux kernel 2.6.30.6 with better
	hardware support and more built-in modules. The boot process has been further
	improved and the boot time is now faster than ever. Major packages found on
	the LiveCD such as Firefox have been updated to the current stable version.
	Home-made GUI boxes have been enhanced. Tazpkg packages manager supports new
	features including the conversion of deb/rpm/arch/slackware/ipk packages to
	Slitaz native format. The ISO image includes a new Openbox, GTK and icons
	theme for an elegant and more beautiful desktop. We have also removed
	ePDFview and switched to an online PDF viewer. This ISO image, however,
	is a bit bigger than 30 Mb (31.5) due to a bigger Kernel and many new
	dependencies.</p>
	<p>We would also like to announce that the SliTaz project has a new
	<a href="http://forum.slitaz.org/">support forum</a> and a new
	<a href="http://scn.slitaz.org/">community platform</a> where
	everyone is welcome to share and to get involved! This new Cooking ISO
	is as always downloadable from the website download section:
	<a href="../get/#cooking">Get SliTaz Cooking</a></p>
	</li>

	<li>
		<strong>19 May 2009 - SliTaz at LinuxDays 2009 (Geneva)</strong>
	<p>
	During the LinuxDays show in Switzerland, the association will have a
	booth in the Association Village and a lecture will be given on Wednesday
	morning. The lecture is a case study with a corporate view based on using a
	"SliTaz Business Server" with a switchboard, integrated ERP, mail, LDAP,
	etc...
	</p>
	<p>
	During the 3 days, the exhibition stand will be manned by members
	of the SliTaz project. The booth will offer demonstrations, including the
	creation of bootable USB keys (LiveUSB) and cdroms. The LinuxDays LiveCD
	will contain a selection of software designed for mobile
