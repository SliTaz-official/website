<h4>Format</h4>

<p>
Messages should be sent as raw text and not HTML, justifying the lines
to 72-80 characters so that people using a mail client in text mode can easily
read them.  The format for these messages can be done automatically by most
good mail clients such as Sylpheed and also allows for more straightforward
reading of the archives. It is also advisable to remove unnecessary citations
and not to attach large files.
</p>

<h3 id="archives">Archives of the list</h3>

<p>
The mailing list is a public forum, all messages are archived and are
searchable online. This allows the non-registrants to still follow the list,
it is also a good resource for information about specific subjects. The list
is archived every night and you can easily browse the archive via the
list engine of TuxFamily. Messages are sorted by month and date or by
discussion thread (<em>Thread</em>) :
<a href="http://listengine.tuxfamily.org/lists.tuxfamily.org/slitaz/"
 >Archives of the Mailing List</a>
</p>

