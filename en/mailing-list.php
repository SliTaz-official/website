<h3 id="about">About the list</h3>

<p>
The discussion/distribution (mailing list) is the main form of communication
in the Slitaz community.  The list is open to everyone, you are free to
subscribe, unsubscribe  at any time.  It is a very effective means of
collaboration, and is used to provide free technical support to users of
SliTaz. Developers, advanced users, proofreaders, etc, use the same list to
manage the development of the project.  Using the same list for support and
development allows us to reach a wider audience, resulting in a faster, more
accurate response. However, the members on the list are all volunteers and do
the best they can based on their free time. Courtesy and politeness are de
rigueur and little bit of humour never hurt anyone.
</p>
<p>
The <em>mailing list</em> is used to send messages and receive mail. This
means that all registered members can post on the mailing list and receive
all messages posted by other members. Messages are sent by the list engine of
<a href="http://www.tuxfamily.org/">TuxFamily</a> powered by the excellent
<a href="http://www.vhffs.org/">VHFFS</a>.
</p>

<h4>Civility</h4>

<p>
The mailing list is used by some often passionate and sensitive people.
Make an extra effort to treat others with respect, even if you feel badly
treated yourself. If you have something negative or critical to say, please
do not post. If a person looks inappropriate contact a Slitaz developer.
</p>

<h3 id="usage">Using the list</h3>

<p>
To be able to post on the list, you must first subscribe, you will then
receive a confirmation message. This mail contains a confirmation number
which you need to return to the list administrators (this prevents spam).
Once the mail and the confirmation message are received, you can then post
on the list and receive all messages that are posted. Note that messages can
be filtered by your preferred mail software into a specific directory. It is
also advisable to use "Reply", to reply to a message so that the discussion
thread can be properly followed.
</p>
